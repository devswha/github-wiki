// Build-time GitHub repo stats collector.
//
// Fetches stars/forks/pushed_at for the given repo slugs and writes them to the
// committed cache (src/wiki/repoStats.generated.ts). Design contract:
//   - Runs at BUILD time only (invoked from the vite.config plugin's buildStart).
//   - Visitors never trigger GitHub API calls.
//   - Fail-safe: any per-repo error (rate limit, network, timeout) keeps the
//     previously cached value. The build never throws because of this step.
//   - The committed cache is the offline/reproducible baseline; every build
//     refreshes it, and the per-row `fetchedAt` date proves freshness.
import { readFile, writeFile } from "node:fs/promises";

const HEADER = `// AUTO-GENERATED by scripts/collect-repo-stats.mjs at build time.
// Committed as the fail-safe baseline; refreshed on every build (buildStart).
// Do not edit by hand.
import type { RepoStat } from "./repoStats";

export const repoStats: Record<string, RepoStat> = `;

const OBJECT_RE = /export const repoStats: Record<string, RepoStat> = (\{[\s\S]*\});\s*$/;

function isRepoSlug(slug) {
  return typeof slug === "string" && /^[^/\s]+\/[^/\s]+$/.test(slug);
}

async function readBaseline(cachePath) {
  try {
    const source = await readFile(cachePath, "utf8");
    const match = source.match(OBJECT_RE);
    if (match) {
      return JSON.parse(match[1]);
    }
  } catch {
    // no baseline yet — start empty
  }
  return {};
}

async function fetchRepo(slug, token, timeoutMs = 8000) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const headers = {
      Accept: "application/vnd.github+json",
      "User-Agent": "github-wiki-stats",
    };
    if (token) {
      headers.Authorization = `Bearer ${token}`;
    }
    const response = await fetch(`https://api.github.com/repos/${slug}`, {
      headers,
      signal: controller.signal,
    });
    if (response.status === 404) {
      return { notFound: true };
    }
    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }
    const json = await response.json();
    return {
      stars: json.stargazers_count,
      forks: json.forks_count,
      pushedAt: json.pushed_at,
    };
  } finally {
    clearTimeout(timer);
  }
}

function serialize(stats) {
  const sorted = {};
  for (const key of Object.keys(stats).sort()) {
    const stat = stats[key];
    sorted[key] = {
      stars: stat.stars,
      forks: stat.forks,
      pushedAt: stat.pushedAt,
      fetchedAt: stat.fetchedAt,
    };
  }
  return `${HEADER}${JSON.stringify(sorted, null, 2)};\n`;
}

export async function collectRepoStats({ slugs, cachePath, token, log = () => {} }) {
  const baseline = await readBaseline(cachePath);
  const result = { ...baseline };
  const today = new Date().toISOString().slice(0, 10);
  let fetched = 0;
  let keptOnError = 0;
  let skipped = 0;

  for (const slug of slugs) {
    if (!isRepoSlug(slug)) {
      skipped += 1;
      continue;
    }
    try {
      const repo = await fetchRepo(slug, token);
      if (repo.notFound) {
        // Unknown/renamed repo: keep any existing baseline, otherwise skip.
        if (result[slug] === undefined) {
          skipped += 1;
        }
        continue;
      }
      result[slug] = {
        stars: repo.stars,
        forks: repo.forks,
        pushedAt: repo.pushedAt,
        fetchedAt: today,
      };
      fetched += 1;
    } catch (error) {
      // Fail-safe: keep the cached value, never break the build.
      keptOnError += 1;
      log(`repo stats: kept cached ${slug} (${error.message})`);
    }
  }

  await writeFile(cachePath, serialize(result), "utf8");
  log(
    `repo stats: ${fetched} fetched, ${keptOnError} kept-on-error, ${skipped} skipped`,
  );
  return result;
}
